        public  boolean digitalCorrect(String input){
        Pattern p = Pattern.compile("^[0-9]{16}$");
        Matcher m = p.matcher(input);
        boolean b = m.matches();
        return b;
    }
        public  boolean pinCorrect(String input){
        Pattern p = Pattern.compile("^[0-9]{3}$");
        Matcher m = p.matcher(input);
        boolean b = m.matches();
        return b;
    }
        public boolean nameCorrect(String input){
            Pattern p = Pattern.compile("^[a-zA-Z ]+$");
            Matcher m = p.matcher(input);
            boolean b = m.matches();
            return b;
        }
      
    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnSubmitActionPerformed
    {//GEN-HEADEREND:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        boolean selectDebit = rbtnDebit.isSelected();
        boolean selectCredit = rbtnCredit.isSelected();
        
        
        if (selectDebit==false && selectCredit==false){
            JOptionPane.showMessageDialog(null, "You should select at least one payment type", "Not Acceptable", JOptionPane.WARNING_MESSAGE);
            return;
        }
        String name = txtName.getText();
        boolean nameFlag = nameCorrect(name);
        boolean nameEmpty = name.length()>0;
        if (!nameEmpty){
            JOptionPane.showMessageDialog(null, "Name should not be empty!!", "Not Acceptable", JOptionPane.WARNING_MESSAGE);
             return;
        }
        if(!nameFlag){
             JOptionPane.showMessageDialog(null, "Name format not acceptable!!", "Not Acceptable", JOptionPane.WARNING_MESSAGE);
             return;
        } 
        String cardNumber =txtAccountNumber.getText();
        boolean flag= digitalCorrect(cardNumber);
        if (!flag){
            JOptionPane.showMessageDialog(null, "Card Number format not acceptable!!", "Not Acceptable", JOptionPane.WARNING_MESSAGE);
            return;
        }
        String recard = txtReenterAccountNumer.getText();
        if (!cardNumber.equals(recard)){
            JOptionPane.showMessageDialog(null, "Card Number are not matched!!", "Not Acceptable", JOptionPane.WARNING_MESSAGE);
            return;
        }
        String date = txtDate.getText();
        String monthString=date.substring(0,2);
        int monthNum = Integer.parseInt(monthString);
        if (monthNum>12 || monthNum<=0){
                 JOptionPane.showMessageDialog(null, "Expiration Date not acceptable!!", "Not Acceptable", JOptionPane.WARNING_MESSAGE);
                return;
             }
        try {
             SimpleDateFormat formatter = new SimpleDateFormat( "MM/YY"); 
          
             Date dateExpire = formatter.parse(date);
             
             Calendar cal = Calendar.getInstance();
             int month = cal.get(Calendar.MONTH) + 1;
             
             int year = cal.get(Calendar.YEAR);
             
             String curr = String.valueOf(month)+"/"+String.valueOf(year);
             Date currDate = formatter.parse(curr);
             Date newDate = new Date();
             
             if(dateExpire.before(newDate)){
                JOptionPane.showMessageDialog(null, "Your card has been Expired! Please try another one.", "Not Acceptable", JOptionPane.WARNING_MESSAGE);
                return;
             }
         } catch (ParseException ex) {
                JOptionPane.showMessageDialog(null, "Your form is not acceptable", "Not Acceptable", JOptionPane.WARNING_MESSAGE);
                return;
         }

        String pin = txtPin.getText();
        flag=pinCorrect(pin);
        if (!flag){
            JOptionPane.showMessageDialog(null, "pin should be 3 digits!!", "Not Acceptable", JOptionPane.WARNING_MESSAGE);
            return;
        }
        JOptionPane.showMessageDialog(null, "Success!!", "Message", JOptionPane.INFORMATION_MESSAGE);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        Component[] componentArray = userProcessContainer.getComponents();        
        for (Component c: componentArray){
            userProcessContainer.remove(c);
        }
        
        
        txtAccountNumber.setText("");
        txtReenterAccountNumer.setText("");
        txtDate.setText("MM/YY");
        txtDate.setForeground(Color.lightGray);
        txtPin.setText("xxx");
        txtPin.setForeground(Color.lightGray);
        txtName.setText("");
        rbtnCredit.setSelected(flag);
        rbtnDebit.setSelected(false);        
        
    }//GEN-LAST:event_btnSubmitActionPerformed

