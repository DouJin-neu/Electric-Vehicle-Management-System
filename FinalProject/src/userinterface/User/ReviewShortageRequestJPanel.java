    public static Organization findOrganization(String username, String password,EcoSystem system){
        Organization organize=null;
        for (Network net : system.getNetworkList()){
            for (Enterprise e : net.getEnterpriseDirectory().getEnterpriseList()){
                for (Organization o:e.getOrganizationDirectory().getOrganizationList()){
                    System.out.println(o.getUserAccountDirectory().getUserAccountList());
                    if (o.getUserAccountDirectory().authenticateUser(username, password)!=null){
                        organize = o;
                    }
                }
            }
        }
        return organize;
    }

