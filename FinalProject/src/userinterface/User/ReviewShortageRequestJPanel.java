    public  boolean digitalCorrect(String input){
            Pattern p = Pattern.compile("^[0-9]+$");
            Matcher m = p.matcher(input);
            boolean b = m.matches();
            return b;
        }
    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnSubmitActionPerformed
    {//GEN-HEADEREND:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        //input validation here
        boolean flag = digitalCorrect(txtNumCP.getText());
        if (!flag){
            JOptionPane.showMessageDialog(null, "Please Enter A Positive Integer Value!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        int number = Integer.parseInt(txtNumCP.getText());
        if (number<5 || number >50){
            JOptionPane.showMessageDialog(null, "Please Enter A Number Between 5 to 50!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Object key = addressCodeMap.keySet().toArray()[0];
        DriverNewLocationRequest request = new DriverNewLocationRequest();
        request.setRequestedLocation(addressCodeMap.get(key));
        request.setFullAddress(addressCodeMap.get(key).getFullAddress());
        request.setNumPiles(number);
        request.setSender(account);
        request.setRequestDate(new Date());
        request.setStatus("PENDING");
        EVDriverOrganization o = (EVDriverOrganization)findOrganization(account.getUsername(), account.getPassword(), system);
        
        o.getDriverNewLocationWorkQueue().add(request);
        JOptionPane.showMessageDialog(null, "Submit Success!", "Success", JOptionPane.INFORMATION_MESSAGE);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        Component[] componentArray = userProcessContainer.getComponents();        
        for (Component c: componentArray){
            userProcessContainer.remove(c);
        }
        UserReportAShortageJPanel panel = new UserReportAShortageJPanel(userProcessContainer, system, account);
        userProcessContainer.add("UserReportAShortageJPanel",panel);
        layout.first(userProcessContainer);
        

        
        
    }//GEN-LAST:event_btnSubmitActionPerformed

