      public void  populateRepairManCombo(){
        
          repairComboBox.removeAllItems();
        for(Organization organization : enterprise.getOrganizationDirectory().getOrganizationList() ){
               if(organization.getType() == Organization.Type.ContractorMaintenance){
                   for(UserAccount userAccount : organization.getUserAccountDirectory().getUserAccountList()){
                        if(userAccount.getIsWorking().booleanValue() == false)
                           repairComboBox.addItem(userAccount);
                       }
                        
                         
                     }
              }
        }

      private void refreshTable() {
        DefaultTableModel model = (DefaultTableModel) table.getModel();
         SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");
            model.setRowCount(0);
           if(maintenanceReqList == null){
                System.out.println(
                      "list empty");
                
           }
            else{
                for (ExecutiveMaintenanceRequest r : maintenanceReqList) {
                    if(r.getRecevingContractor() == enterprise){
                        Object row[] = new Object[7]; 
                        row[0] = r;
                        row[1] = r.getSendingCompany();
                        row[2] = r.getReceiver();
                        row[3] = format.format(r.getRequestDate());
                        row[4] = r.getRequestedStation();
                        row[5] = r.getRequestedPile();
                        row[6] = r.getStatus();
                        model.addRow(row);
                    }
              
                }
           }
    }

