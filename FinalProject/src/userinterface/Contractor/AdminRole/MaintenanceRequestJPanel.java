    private void assignJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assignJButtonActionPerformed
        int selectedRow = table.getSelectedRow();

        if (selectedRow < 0){
            return;
        }

        WorkRequest request = (WorkRequest)table.getValueAt(selectedRow, 0); 
       
        request.setReceiver((UserAccount)repairComboBox.getSelectedItem());
        request.setStatus("SENT");
        UserAccount mainAccount = (UserAccount)repairComboBox.getSelectedItem();
        mainAccount.setIsWorking(true);
        
       // populateTable();
       refreshTable();
       populateRepairManCombo();
    }//GEN-LAST:event_assignJButtonActionPerformed

